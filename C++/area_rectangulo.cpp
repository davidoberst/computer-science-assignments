//Area Rectangulo = multiplica la medida de su base por la medida de su altura 
#include <iostream>
int main(){
    int base;
    int altura;
    std::cout<<"Ingresa la base : ";std::cin>>base;
    std::cout<<"Ingresa la altura : ";std::cin>>altura;
    std::cout<<"el area del rectangulo es : " << base*altura;
    return 0;
}